  space_id UUID NOT NULL,
  group_id UUID NOT NULL,
  group_task_id UUID,
  day_of_week INTEGER,
  start_time TIME,
  end_time TIME,
  required_headcount INTEGER,
  is_deleted BOOLEAN NOT NULL DEFAULT FALSE,
  created_by_user_id UUID,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

ALTER TABLE shift_templates
  ADD COLUMN IF NOT EXISTS group_task_id UUID,
  ADD COLUMN IF NOT EXISTS day_of_week INTEGER,
  ADD COLUMN IF NOT EXISTS start_time TIME,
  ADD COLUMN IF NOT EXISTS end_time TIME,
  ADD COLUMN IF NOT EXISTS required_headcount INTEGER,
  ADD COLUMN IF NOT EXISTS is_deleted BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS created_by_user_id UUID;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_templates' AND column_name = 'starts_at_time'
  ) THEN
    EXECUTE '
      UPDATE shift_templates
      SET start_time = COALESCE(start_time, starts_at_time)
      WHERE start_time IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_templates' AND column_name = 'ends_at_time'
  ) THEN
    EXECUTE '
      UPDATE shift_templates
      SET end_time = COALESCE(end_time, ends_at_time)
      WHERE end_time IS NULL';
  END IF;

  UPDATE shift_templates
  SET
    required_headcount = COALESCE(required_headcount, 1),
    day_of_week = COALESCE(day_of_week, 0);

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_templates' AND column_name = 'name'
  ) THEN
    EXECUTE '
      UPDATE shift_templates st
      SET group_task_id = t.id
      FROM tasks t
      WHERE st.group_task_id IS NULL
        AND t.group_id = st.group_id
        AND t.name = st.name';
  END IF;
END $$;

CREATE INDEX IF NOT EXISTS idx_shift_templates_group
  ON shift_templates(group_id)
  WHERE is_deleted = FALSE;

-- Shift slots
CREATE TABLE IF NOT EXISTS shift_slots (
  id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
  space_id UUID NOT NULL,
  group_id UUID NOT NULL,
  group_task_id UUID,
  shift_template_id UUID,
  scheduling_cycle_id UUID,
  date DATE,
  start_time TIME,
  end_time TIME,
  capacity INTEGER,
  current_fill_count INTEGER NOT NULL DEFAULT 0,
  status TEXT NOT NULL DEFAULT 'Open',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

ALTER TABLE shift_slots
  ADD COLUMN IF NOT EXISTS group_task_id UUID,
  ADD COLUMN IF NOT EXISTS shift_template_id UUID,
  ADD COLUMN IF NOT EXISTS scheduling_cycle_id UUID,
  ADD COLUMN IF NOT EXISTS date DATE,
  ADD COLUMN IF NOT EXISTS start_time TIME,
  ADD COLUMN IF NOT EXISTS end_time TIME,
  ADD COLUMN IF NOT EXISTS capacity INTEGER,
  ADD COLUMN IF NOT EXISTS current_fill_count INTEGER NOT NULL DEFAULT 0;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'cycle_id'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET scheduling_cycle_id = COALESCE(scheduling_cycle_id, cycle_id)
      WHERE scheduling_cycle_id IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'template_id'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET shift_template_id = COALESCE(shift_template_id, template_id)
      WHERE shift_template_id IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'starts_at'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET
        date = COALESCE(date, starts_at::date),
        start_time = COALESCE(start_time, starts_at::time)
      WHERE date IS NULL OR start_time IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'ends_at'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET end_time = COALESCE(end_time, ends_at::time)
      WHERE end_time IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'max_headcount'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET capacity = COALESCE(capacity, max_headcount)
      WHERE capacity IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'required_headcount'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET capacity = COALESCE(capacity, required_headcount)
      WHERE capacity IS NULL';
  END IF;

  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'shift_slots' AND column_name = 'current_headcount'
  ) THEN
    EXECUTE '
      UPDATE shift_slots
      SET current_fill_count = COALESCE(current_fill_count, current_headcount, 0)';
  END IF;

  UPDATE shift_slots
  SET capacity = COALESCE(capacity, 1);

  UPDATE shift_slots ss
  SET group_task_id = st.group_task_id
  FROM shift_templates st
  WHERE ss.group_task_id IS NULL
    AND ss.shift_template_id = st.id;
END $$;

CREATE INDEX IF NOT EXISTS idx_shift_slots_cycle
