UPDATE shift_requests sr
SET
  group_id = COALESCE(sr.group_id, ss.group_id),
  scheduling_cycle_id = COALESCE(sr.scheduling_cycle_id, ss.scheduling_cycle_id)
FROM shift_slots ss
WHERE sr.shift_slot_id = ss.id
  AND (sr.group_id IS NULL OR sr.scheduling_cycle_id IS NULL);

